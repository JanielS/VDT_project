import { formulasSchema, type FormulaDefinition } from '../types/formula';

const sum = (id: string, label: string, dependencies: string[]): FormulaDefinition => ({
  id,
  label,
  expression: dependencies.join(' + '),
  dependencies,
  status: 'validated',
  description: 'Soma dos indicadores filhos conforme estrutura do fluxo de valor.',
});

const qty = (id: string, label: string, consumptionId: string): FormulaDefinition => ({
  id,
  label,
  expression: `${consumptionId} * wet_feed_mass / 1000`,
  dependencies: [consumptionId, 'wet_feed_mass'],
  status: 'validated',
  description: 'Quantidade calculada por consumo específico multiplicado pela massa de alimentação da via úmida.',
});

const cost = (id: string, label: string, quantityId: string, priceId: string): FormulaDefinition => ({
  id,
  label,
  expression: `${quantityId} * ${priceId} / 1000`,
  dependencies: [quantityId, priceId],
  status: 'validated',
  description: 'Custo calculado por quantidade multiplicada pelo preço, convertido para kUSD.',
});

